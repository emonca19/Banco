    //limita las fechas del calendario solamente las peronas mayores a 18 y menores a 120 pueden crear una cuenta
    public void limitarCalendario() {
        Calendar calendar = Calendar.getInstance();
        Date fechaActual = calendar.getTime();

        // Calcular la fecha mínima (hace 120 años desde la fecha actual)
        calendar.add(Calendar.YEAR, -120);
        Date fechaMinima = calendar.getTime();

        // Calcular la fecha máxima (hace 18 años desde la fecha actual)
        calendar.setTime(fechaActual);
        calendar.add(Calendar.YEAR, -18);
        Date fechaMaxima = calendar.getTime();

        // Establecer el rango de fechas seleccionables
        dateChooser.setSelectableDateRange(fechaMinima, fechaMaxima);
    }

    //inicializo los datos con los valores ingresados en el jframe
    private void inicializarDatos() {
        nombres = txtNombre.getText();
        apellidoPaterno = txtApellidoPaterno.getText();
        apellidoMaterno = txtApellidoMaterno.getText();
        calle = txtCalle.getText();
        colonia = txtColonia.getText();
        codigoPostal = txtCodigoPostal.getText();
        fechaNacimiento = dateChooser.getDate();
        correo = txtCorreo.getText();
        contraseña = txtContraseña.getText();
    }



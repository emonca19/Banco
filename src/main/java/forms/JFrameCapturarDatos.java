    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        // TODO add your handling code here:
        // Verificar si algún campo está vacío

        char[] contraseniaChar = txtContraseña.getPassword();
        String contrasenia = new String(contraseniaChar);
        contraseniaChar = txtConfirmarContraseña.getPassword();
        String contraseniaConfirmar = new String(contraseniaChar);
        
        //verifica que todos los datos sean ingresados
        if (txtCodigoPostal.getText().isEmpty()
                || txtCalle.getText().isEmpty()
                || txtColonia.getText().isEmpty()
                || txtNombre.getText().isEmpty()
                || txtApellidoPaterno.getText().isEmpty()
                || txtApellidoMaterno.getText().isEmpty()
                || txtCorreo.getText().isEmpty()
                || txtContraseña.getPassword().length == 0
                || txtConfirmarContraseña.getPassword().length == 0
                || dateChooser == null){
            // Al menos un campo está vacío
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
        }else // verifica que la contraseña y el confirmar contraseña sean iguales
        if (!contrasenia.equals(contraseniaConfirmar)) {
            JOptionPane.showMessageDialog(this, "La contraseña no coincide, porfavor verifique", "Contraseña no coincide", JOptionPane.WARNING_MESSAGE);
        }else {
            //verifica que el usuario no este ocupado
            
            VerificarClienteDTO verificar = new VerificarClienteDTO();
            
            Cliente cliente = new Cliente(txtCorreo.getText(), contrasenia);
             
            if (verificar.verificarCorreoExistente(cliente)) {
                JOptionPane.showMessageDialog(this, "Correo ya en uso", "Cliente ya registrado", JOptionPane.WARNING_MESSAGE);
                //si no esta ocupado guarda la cuenta
            } else if(!verificar.verificarCorreoExistente(cliente)) {

                ClienteDTO clienteDTO = new ClienteDTO();
                IClienteDAO clienteDAO = new ClienteDAO(conexionBD);
                //pone los datos del cliente
                inicializarDatos();
                // guarda al cliente
                clienteDTO.setNombres(nombres);
                clienteDTO.setApellidoPaterno(apellidoPaterno);
                clienteDTO.setApellidoMaterno(apellidoMaterno);
                clienteDTO.setCalle(calle);
                clienteDTO.setColonia(colonia);
                clienteDTO.setCodigoPostal(codigoPostal);
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy"); // Ajusta el formato según tus necesidades
                String fechaNacimientoString = sdf.format(fechaNacimiento);
                clienteDTO.setFechaNacimiento(fechaNacimientoString);
                clienteDTO.setCorreo(correo);
                clienteDTO.setContrasenia(contraseña);
                // inicializa de nuevo los datos ahora con los id creados
                clienteDAO.guardar(clienteDTO);
                inicializarDatos();
                JOptionPane.showMessageDialog(this, "Bienvenido,¡te has ingresado con exito!", "Bienvenido" + " " + txtNombre.getText(), JOptionPane.PLAIN_MESSAGE);
                dispose();
                //entra a menu de cliente registrado
                JFrameCrearCuenta jFrameCrearCuenta = new JFrameCrearCuenta(this, conexionBD);
                jFrameCrearCuenta.setVisible(true);
                this.dispose();
                
            }
        }

        


    }//GEN-LAST:event_btnSiguienteActionPerformed


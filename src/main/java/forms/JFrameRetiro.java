    private void txtAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAceptarActionPerformed
        // TODO add your handling code here:
        
        if(Integer.parseInt(txtCantidadRetiro.getText()) > cuentaDTO.getSaldo()){
            
            JOptionPane.showMessageDialog(this, "No puedes retirar esa cantidad", "ERROR!!", JOptionPane.ERROR_MESSAGE);
            
        }else if (Integer.parseInt(txtCantidadRetiro.getText()) < 0 ){
            
            JOptionPane.showMessageDialog(this, "No puedes retirar cantidades negativas", "ERROR!!", JOptionPane.ERROR_MESSAGE);
            
        }else {
            
            int saldo = cuentaDAO.retirarDinero(cuentaDTO.getNumeroCuenta(), Integer.parseInt(txtCantidadRetiro.getText()));
            cuentaDTO.setSaldo(saldo);
            txtSaldoActual.setText(String.valueOf(cuentaDTO.getSaldo()));
            JOptionPane.showMessageDialog(this, "El retiro se realizÃ³ correctamente \nSaldo disponible: " + cuentaDTO.getSaldo(), "EXITO!!", JOptionPane.INFORMATION_MESSAGE);
            JFrameCliente jFrameCliente = new JFrameCliente(cuentaDTO);
            jFrameCliente.setVisible(true);
            this.dispose();
            
        }
        
    }//GEN-LAST:event_txtAceptarActionPerformed

